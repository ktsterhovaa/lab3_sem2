#ifndef LAB3_FUNCTIONS_H
#define LAB3_FUNCTIONS_H
class Functions {

public:
    static int Add(int a, int b) {
        return a + b;
    }
    static int Sub(int a, int b) {
        return a - b;
    }
    static int Mult(int a, int b) {
        return a * b;
    }

};
#endif //LAB3_FUNCTIONS_H
